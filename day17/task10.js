// Example 1: Basic concatenation
let age = 20;
let message = "Age: " + age;
console.log(message); // Output: "Age: 20"

// Example 2: Concatenating multiple values
let Name = "sangee";
let greeting = "Hello, " + Name + "! You are " + age + " years old.";
console.log(greeting); // Output: "Hello, Alice! You are 20 years old."

// Example 3: Concatenation with calculations
let nextYear = "Next year, you will be " + (age + 1) + " years old.";
console.log(nextYear); // Output: "Next year, you will be 21 years old."
