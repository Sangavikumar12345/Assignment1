// Declare a variable with undefined
let myVar;  // undefined by default

// Declare a variable with null
let emptyVar = null;

// Print the values
console.log("myVar:", myVar);       // undefined
console.log("emptyVar:", emptyVar); // null

// Print their types
console.log("Type of myVar:", typeof myVar);       // undefined
console.log("Type of emptyVar:", typeof emptyVar); // object (JS quirk)
